#include <iostream>
using namespace std;

int main() {
    cout << "The factorial of a positive integer \nis equal to the product of the positive \nintegers from 1 to that number." << endl;
    cout << "\nHere are the factorial results for numbers 1-5" << endl;
    cout << "1: " << (1 * 1) << endl;
    cout << "2: " << (1 * 2) << endl;
    cout << "3: " << (1 * 2 * 3) << endl;
    cout << "4: " << (1 * 2 * 3 * 4) << endl;
    cout << "5: " << (1 * 2 * 3 * 4 * 5) << endl;
    
    return 0;
}
